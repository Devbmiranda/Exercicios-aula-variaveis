// Dados para o cálculo
let raio = 5; // raio do círculo

// Cálculos
let areaDoCirculo = Math.PI * Math.pow(raio, 2);
let perimetroDoCirculo = 2 * Math.PI * raio;

// Exibindo os resultados no console
console.log("Raio do círculo:", raio);
console.log("Área do círculo:", areaDoCirculo.toFixed(2));
console.log("Perímetro do círculo:", perimetroDoCirculo.toFixed(2));